    private void surfaceSizeActiveTextFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_surfaceSizeActiveTextFieldFocusLost
        String text = surfaceSizeActiveTextField.getText();
        try {
            float num = Float.parseFloat(text);
            surfaceSizeActiveTextField.setText(Float.toString(num));
        } catch (Exception e) {
            CommonManager.showMessageDialog(this, "알림", "숫자만 입력할 수 있습니다.", JOptionPane.WARNING_MESSAGE);            
            surfaceSizeActiveTextField.setText("0.0");
        }
    }//GEN-LAST:event_surfaceSizeActiveTextFieldFocusLost

